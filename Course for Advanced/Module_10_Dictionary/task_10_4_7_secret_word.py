word = input()
result = list(word)
n = int(input())
decoder = {}
for _ in range(n):
    s = input()
    key, value = int(s[-1]), s[0]
    for c in word:
        if word.count(c) == key:
            decoder[c] = value
            continue

for i in range(len(word)):
    result[i] = decoder[result[i]]

print(*result, sep='')
