def choose_plural(amount, declensions):
    if 5 <= amount % 10 <= 9 or amount % 10 == 0 or 11 <= amount % 100 <= 14:
        result = f'{amount} {declensions[2]}'
    elif 2 <= amount % 10 <= 4:
        result = f'{amount} {declensions[1]}'
    else:
        result = f'{amount} {declensions[0]}'
    return result


