def dict_travel(nested_dicts):
    for k, v in sorted(nested_dicts.items()):
        if isinstance(v, dict):
            new_dict = {f'{k}.{k_in}': v_in for k_in, v_in in v.items()}
            dict_travel(new_dict)
        else:
            print(f'{k}: {v}')


