'''
Напишите программу с использованием рекурсивной функции, которая выводит изображение песочных часов,
составленное из цифр 1, 2, 3, и 4:

1111111111111111    # 16 раз
  222222222222      # 12 раз
    33333333        # 8 раз
      4444          # 4 раза
    33333333        # 8 раз
  222222222222      # 12 раз
1111111111111111    # 16 раз
'''


def hourglas(digit=1, width=16, space=0):
    line = ' ' * space + str(digit) * width

    if digit == 4:  # Центр — печатаем и останавливаемся
        print(line)
    else:
        # Верх: печатаем строку → углубляемся в рекурсию
        print(line)
        hourglas(digit + 1, width - 4, space + 2)
        # Низ: после выхода из рекурсии печатаем строку ещё раз
        print(line)

hourglas()