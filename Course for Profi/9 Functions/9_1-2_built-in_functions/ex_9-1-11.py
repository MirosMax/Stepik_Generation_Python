data = input()

al_upper = []
al_lower = []
dig_odd = []
dig_even = []

for i in data:
    if i.isalpha():
        if i.isupper():
            al_upper.append(i)
        elif i.islower():
            al_lower.append(i)
    else:
        num = int(i)
        if num % 2 == 0:
            dig_even.append(num)
        else:
            dig_odd.append(num)

new_data = map(sorted, (al_lower, al_upper, dig_odd, dig_even))
for i in new_data:
    print(*i, sep='', end='')
